def area(a, b):
    '''
    Returns the area of a rectangle with 2 given sides of a and b

    Parameters:
        a: (int) - first side of a rectangle
        b: (int) - second side of a rectangle
    
    Return values:
        area(a, b): (int) - area of a rectangle
    '''
    return a * b

def perimeter(a, b):
    return (a + b) * 2