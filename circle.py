import math


def area(r):
    '''
    Return the area of a circle with radius r
    
    Parametrs:
        r: (float) - radius of a circle
    
    Return values:
        area(r): (float) - area of a circle
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Return the perimeter of a circle with radius r
    
    Parametrs:
        r: (float) - perimeter of a circle
    
    Return values:
        perimeter(r): (float) - perimeter of a circle
    '''
    return 2 * math.pi * r

