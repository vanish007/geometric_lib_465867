def area(a, h):
    '''
    Returns the area of a triangle with given base and height

    Parameters:
        a: (float) - base of a rectangle
        h: (float) - height of a rectangle
    
    Return values:
        area(a, h): (float) - area of a rectangle
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Returns the perimeter of a triangle with 3 given sides

    Parameters:
        a: (float) - first side of a rectangle
        b: (float) - second side of a rectangle
        c: (float) - third side of a rectangle
    
    Return values:
        perimeter(a, b, c): (float) - perimeter of a rectangle
    '''
    return a + b + c